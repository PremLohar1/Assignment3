import math
num=int(input('Enter The Number '))
print('\n')
if num>=0:
    root=math.sqrt(num)
    print('The Squre Root Is = ',root)
else:
    print('Enter The Positive Number')    
    
lognum = math.log(num) 
print('Natural logarithm = ',lognum) 

sinnum = math.sin(num) 
print('Sine of the number = ',sinnum) 
    